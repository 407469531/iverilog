# include "device.h"
# include  <string.h>
# include <assert.h>


device_t device_from_arch(const char*arch)
{
      unsigned idx;

      assert(arch);

      for (idx = 0 ;  edif_device_table[idx].name ;  idx += 1) {
	    if (strcmp(arch, edif_device_table[idx].name) == 0)
		  return edif_device_table[idx].driver;

      }

      return 0;
}
